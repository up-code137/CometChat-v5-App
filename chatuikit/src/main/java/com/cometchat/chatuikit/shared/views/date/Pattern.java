package com.cometchat.chatuikit.shared.views.date;

/**
 * Enum representing different patterns for formatting dates.
 */
public enum Pattern {
    TIME, // Time pattern (e.g., "h:mm a")
    DAY_DATE, // Day and date pattern (e.g., "dd MMM yyyy")
    DAY_DATE_TIME // Day, date, and time pattern (e.g., "h:mm a", "dd MMM yyyy", "EEE")
}
